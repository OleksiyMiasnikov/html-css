function initTagsFunction() {
  let tags = new Set();
  for (let i = 0; i < newCupons.length; i++) {
    for (let j = 0; j < newCupons[i].tags.length; j++) {
      tags.add(newCupons[i].tags[j]);
    }
  }
  return Array.from(tags).sort((a, b) => {
    if (a.toLowerCase() < b.toLowerCase()) return -1;
    if (a.toLowerCase() > b.toLowerCase()) return 1;
    return 0;
  });
}
